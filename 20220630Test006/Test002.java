public class Test002
{
	public static void main(String[] args)
	{
		System.out.println("안녕하세요");
		System.out.println("박원석입니다.");
		System.out.println("오늘은 6월 29일 입니다.");
		
		// 실행의 대상은 .class 파일이다. 새롭게 .java에서 내용이 바뀌어도 컴파일을 새로 하지 않으면 제대로 출력되지 않는다.

		System.out.print("반갑습니다.");
		System.out.print("화이팅합시다.");

		// println은 개행 O , print는 개행 X
		
		//『println()』과 『print()』메소드는
		// 같은 상자에서 꺼낸 유사한 도구이지만
		// 이 도구는 서로 다른 도구이며,
		// 두 메소드의 차이는 라인 스킵(줄바꿈) 유무

	}
}

//실행결과
/*
안녕하세요
박원석입니다.
오늘은 6월 29일 입니다.
반갑습니다.화이팅합시다.계속하려면 아무 키나 누르십시오 . . .
*/