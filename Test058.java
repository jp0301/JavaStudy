// 1부터 100 까지의 정수 중에서
// 4의 배수만 출력하는 프로그램을 구현한다.
// 단,  for 문을 활용해야 하며
// 한 줄에 5개씩만 출력할 수 있도록 한다.

// 실행 예)
//  4     8     12     16     20
// 24    28     32     36     40
//              :
// 계속하려면 아무 키나 누르세요...


public class Test058
{
	public static void main(String[] args)
	{
		
		for(int n = 1; n <= 100; n++)
		{
			if(n % 4 == 0)
			{
				System.out.print(" " + n);

				if(n % 10 == 0)
					System.out.println();
			}
		}
	


	}
}